package com.codegnan.collections;

public class AutoBoxingAutoUnboxing {

	public static void main(String[] args) {
		int a = 5;
		Integer obj = 5;
		
		System.out.println("Primitve type: "+a);
		System.out.println("Wrapper object: "+obj);
		
		Double bobj = 50.9;
		double b = 50.9;
		
		System.out.println("Wrapper object: "+bobj);
		System.out.println("Primitve type: "+b);
	}

}
